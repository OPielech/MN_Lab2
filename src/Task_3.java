    public static double trueValueFx(int m, double h) {
        double x = 1 + h;
        double fx = Math.pow(x, m);
        return fx;
    }//end of trueValueFx

    public static double fxZeroDerivative(int m) {
        double xi = 1;
        double fx = Math.pow(xi, m);
        return fx;
    }//end of fx

    public static double fxFirstDerivative(int m) {
        double xi = 1;
        double fxFirstDerivative = m * Math.pow(xi, m - 1);
        return fxFirstDerivative;
    }//end of fxFirstDerivative

    public static double remainder(int m, double h) {
        double remainder = 0;

        double xi = 1;
        if (m == 1)
            remainder = 0;
        else if (m == 2)
            remainder = (Math.pow(m, 2) - m) * Math.pow(xi, m - 2) / 2 * Math.pow(h, 2);
        else if (m == 3)
            remainder = (Math.pow(m, 2) - m) * Math.pow(xi, m - 2) / 2 * Math.pow(h, 2)
                    + (Math.pow(m, 3) - 3 * Math.pow(m, 2) + 2 * m) * Math.pow(xi, m - 3) / 6 * Math.pow(h, 3);
        else if (m == 4)
            remainder = (Math.pow(m, 2) - m) * Math.pow(xi, m - 2) / 2 * Math.pow(h, 2)
                    + (Math.pow(m, 3) - 3 * Math.pow(m, 2) + 2 * m) * Math.pow(xi, m - 3) / 6 * Math.pow(h, 3)
                    + (Math.pow(m, 4) - 6 * Math.pow(m, 3) + 11 * Math.pow(m, 2) - 6 * m) * Math.pow(xi, m - 4) / 24 * Math.pow(h, 4);


        return remainder;
    }//end of Remainder


    public static double taylorSeries(int m, double h) {
        double approximation = fxZeroDerivative(m) + fxFirstDerivative(m) * h;
        return approximation;
    }//end of taylorSeries
